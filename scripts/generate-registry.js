const fs = require('fs');
const path = require('path');

const applicationsDir = path.join(__dirname, '../src/data/applications');
const registryFile = path.join(__dirname, '../src/lib/applications-registry.ts');

// Ensure parent directories exist
fs.mkdirSync(path.dirname(registryFile), { recursive: true });

// Read all JSON files in the applications directory, excluding those starting with '_'
const files = fs.readdirSync(applicationsDir)
  .filter(file => file.endsWith('.json') && !file.startsWith('_'));

const imports = [];
const registryEntries = [];

files.forEach((file, index) => {
  const slug = path.basename(file, '.json');
  const varName = `config_${index}`;
  imports.push(`import ${varName} from "@/data/applications/${file}";`);
  registryEntries.push(`  "${slug}": ${varName},`);
});

const content = `// This file is auto-generated by scripts/generate-registry.js
// Do not edit this file manually.

${imports.join('\n')}

export const applicationsRegistry: Record<string, unknown> = {
${registryEntries.join('\n')}
};
`;

fs.writeFileSync(registryFile, content, 'utf8');
console.log(`Successfully generated applications registry with ${files.length} applications.`);
